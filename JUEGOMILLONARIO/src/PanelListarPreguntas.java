    private void textoBuscar(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textoBuscar
        // TODO add your handling code here:
         modelo1.removeAllElements();
        for(int i=0;i<ventana.getGestionPreguntas().getArrayPreguntas().size();i++){
              
            
                if(ventana.getGestionPreguntas().getArrayPreguntas().get(i).getPregunta().contains(txtBuscar.getText())){

                    modelo1.addElement(ventana.getGestionPreguntas().getArrayPreguntas().get(i).getPregunta());
                    
                }
              }
        
    }//GEN-LAST:event_textoBuscar

