    public void cargarModeloLista(){
        for(int i=0;i<ventana.getGestionPreguntas().getArrayPreguntas().size();i++){

            modelo1.addElement(ventana.getGestionPreguntas().getArrayPreguntas().get(i).getPregunta());
       
        }
        
        
        
        list1.setModel(modelo1);
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        modelo1.removeAllElements();
        
        
        int seleccion = combobox1.getSelectedIndex();
        
        if(seleccion==0){
            ventana.getGestionPreguntas().ordenarArrayPreguntas();
        }
        else if(seleccion==1){
             ventana.getGestionPreguntas().ordenarArrayListNombrePregunta();
        }
         
         
         
        cargarModeloLista();
         
    }//GEN-LAST:event_jButton2ActionPerformed

